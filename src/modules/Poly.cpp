void Poly::draw(GLSLProgram *shader, glm::mat4 view, glm::mat4 proj)
{
	shader->use();

	glBindVertexArray(vao);

	GLfloat timeValue = glfwGetTime() / 3;
	GLfloat greenValue = (sin(timeValue) / 2) + 0.5;

	shader->setUniform("lightColor", glm::vec3(1.0f, 1.0f, 1.0f));
	shader->setUniform("objColor", glm::vec3(1.0f, 0.5f, 0.31f));

	shader->setUniform("V", view);

	shader->setUniform("P", proj);

	GLfloat angle = (GLfloat)glfwGetTime() * 0.0f;

	glm::mat4 model = glm::mat4(1.0f);

	model = glm::translate(model, glm::vec3(1.0f, -1.0f, -2.0f));
	model = glm::rotate(model, angle, glm::vec3(1.0f, 1.0f, -1.0f));
	model = glm::scale(model, glm::vec3(3.0f));
	shader->setUniform("M", model);

	shader->setUniform("N", glm::mat3(glm::vec3(model[0]), glm::vec3(model[1]), glm::vec3(model[2])));

	glDrawArrays(GL_TRIANGLES, 0, 36);

}
