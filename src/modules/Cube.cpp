void Cube::draw(GLSLProgram *shader, glm::mat4 view, glm::mat4 proj)
{
	shader->use();

	glBindVertexArray(vao);

	GLfloat timeValue = glfwGetTime() / 3;
	GLfloat greenValue = (sin(timeValue) / 2) + 0.5;

	shader->setUniform("material.ambient", color * ambient);
	shader->setUniform("material.diffuse", color);
	shader->setUniform("material.specular", color * specular);
	shader->setUniform("material.shininess", shininess);

	shader->setUniform("V", view);

	shader->setUniform("P", proj);

	glm::mat4 model = glm::mat4(1.0f);
	model = glm::translate(model, position);
	model = glm::rotate(model, rotationAngle, rotationAxis);
	model = glm::scale(model, scale);
	shader->setUniform("M", model);

	glm::mat4 modelView = view * model;
	shader->setUniform("N", glm::mat3(glm::vec3(modelView[0]), glm::vec3(modelView[1]), glm::vec3(modelView[2])));

	glDrawArrays(GL_TRIANGLES, 0, 36);
}
